/*
    Operadores de asignación
    =
    Operador compuesto
    + = 
    - =
    * =
    / =
    % =

*/
let numero = 10;
//Operador de asignación compuesto +=
numero += 5;
console.log(numero);
//Equivalente
//numero = numero + 5;
//Operador de asignación compuesto -=
numero-=2;
console.log(numero);
//Operacion compuesta *=
numero*=2;
console.log(numero);
//Operacion compuesta /=
numero/=2;
console.log(numero);
//Operacion compuesta %=
numero%=2;
console.log(numero);
//Operacion compuesta **=
numero = 2;
numero **=2;
console.log(numero);